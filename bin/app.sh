#!/bin/bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

source "$SCRIPT_DIR/config.sh"

NETWORK_APPS="${ENABLE_NETWORK_APPS:-1}"
TTS_APPS="${ENABLE_TTS:-${WITH_TTS:-0}}"
if [[ "$TTS_APPS" != "0" && "$TTS_APPS" != "1" ]]; then
  printf 'ENABLE_TTS/WITH_TTS must be 0 or 1, got %q\n' "$TTS_APPS" >&2
  exit 1
fi
BUILD_PROFILE=$([[ "$NETWORK_APPS" != "0" ]] && printf 'network' || printf 'no-network')
if [[ "$TTS_APPS" != "0" ]]; then
  BUILD_PROFILE+="-tts"
fi
BUILD_DIR="$PROJECT_DIR/build/$BUILD_PROFILE"
SKETCH_DIR="$PROJECT_DIR/src"
APP_BIN="$BUILD_DIR/src.ino.bin"
MERGED_BIN="$BUILD_DIR/src.ino.merged.bin"
PARTITIONS_BIN="$BUILD_DIR/src.ino.partitions.bin"
APP_PARTITION_SIZE_BYTES=$((0x7E0000))
PARTITIONS_CSV="$SKETCH_DIR/partitions.csv"
ALL_MODULES_CPP="$SKETCH_DIR/all_modules.cpp"
SECRETS_CONFIG_H="$SKETCH_DIR/secrets_config.h"
AUTOGEN_PREFIX="__autogen_module_"
FLASH_PARTITIONS_READ=""

log() {
  printf '\n[%s] %s\n' "$(date '+%H:%M:%S')" "$*"
}

log "Project: $PROJECT_DIR"
log "Sketch:  $SKETCH_DIR"
log "Build:   $BUILD_DIR"
log "Port:    $PORT"
log "FQBN:    $FQBN"

"$PROJECT_DIR/tools/generate-secrets-header.sh"

cleanup() {
  rm -f "$ALL_MODULES_CPP"
  rm -f "$SECRETS_CONFIG_H"
  if [[ -n "$FLASH_PARTITIONS_READ" ]]; then
    rm -f "$FLASH_PARTITIONS_READ"
  fi
}
trap cleanup EXIT

log "Generating compile wrappers for nested source files"
rm -f "$ALL_MODULES_CPP"
index=0
write_if_changed() {
  local path="$1"
  local tmp
  tmp="$(mktemp "${TMPDIR:-/tmp}/pocketink-wrapper.XXXXXX")"
  cat > "$tmp"
  if [[ -f "$path" ]] && cmp -s "$tmp" "$path"; then
    rm -f "$tmp"
  else
    mv "$tmp" "$path"
  fi
}
emit_wrapper() {
  local file="$1"
  local rel_path ext wrapper
  rel_path="${file#"$SKETCH_DIR"/}"
  ext="${file##*.}"
  if [[ "$ext" != "c" ]]; then
    ext="cpp"
  fi
  wrapper="$SKETCH_DIR/${AUTOGEN_PREFIX}${index}.${ext}"
  write_if_changed "$wrapper" <<EOF
// Auto-generated by bin/app.sh for Arduino sketch builds.
// Do not edit manually.
#include "$rel_path"
EOF
  index=$((index + 1))
}

while IFS= read -r file; do
  emit_wrapper "$file"
done < <(
  find "$SKETCH_DIR" -mindepth 2 -type f -name '*.cpp' \
    ! -name 'src.ino.cpp' \
    ! -name 'all_modules.cpp' \
    ! -name "${AUTOGEN_PREFIX}*.cpp" \
    ! -path "$SKETCH_DIR/apps/tts_app.cpp" \
    ! -path "$SKETCH_DIR/tts/*" \
    ! -path "$SKETCH_DIR/InflectNanoTTS/*" \
    | sort
)

if [[ "$TTS_APPS" != "0" ]]; then
  INFLECT_SOURCES=(
    "apps/tts_app.cpp"
    "tts/ggml_backend_registry_esp32.cpp"
    "tts/inflect_tts_wrapper.cpp"
    "tts/tts_ggml_psram_buffer.cpp"
    "tts/InflectNanoTTS/src/synthesizer.cpp"
    "tts/InflectNanoTTS/src/griffin_lim_vocoder.cpp"
    "tts/InflectNanoTTS/src/model_loader.cpp"
    "tts/InflectNanoTTS/src/acoustic_model.cpp"
    "tts/InflectNanoTTS/src/vocoder_model.cpp"
    "tts/InflectNanoTTS/src/text_frontend.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml.c"
    "tts/InflectNanoTTS/ggml/src/ggml.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-alloc.c"
    "tts/InflectNanoTTS/ggml/src/ggml-backend.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-backend-meta.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-threading.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-quants.c"
    "tts/InflectNanoTTS/ggml/src/gguf.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/ggml-cpu.c"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/ggml-cpu.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/repack.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/hbm.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/quants.c"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/traits.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/binary-ops.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/unary-ops.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/vec.cpp"
    "tts/InflectNanoTTS/ggml/src/ggml-cpu/ops.cpp"
  )
  for rel_path in "${INFLECT_SOURCES[@]}"; do
    emit_wrapper "$SKETCH_DIR/$rel_path"
  done
fi
for stale in "$SKETCH_DIR"/${AUTOGEN_PREFIX}*.cpp "$SKETCH_DIR"/${AUTOGEN_PREFIX}*.c; do
  [[ -e "$stale" ]] || continue
  base="${stale##*/${AUTOGEN_PREFIX}}"
  num="${base%%.*}"
  if [[ "$num" =~ ^[0-9]+$ && "$num" -ge "$index" ]]; then
    rm -f "$stale"
  fi
done

COMPILE_ARGS=(arduino-cli compile --fqbn "$FQBN" --build-path "$BUILD_DIR")
INFLECT_FLAGS=""
append_inflect_flag() {
  local name="$1"
  local default_value="$2"
  local value="${!name:-$default_value}"
  if [[ "$value" != "0" && "$value" != "1" ]]; then
    printf '%s must be 0 or 1, got %q\n' "$name" "$value" >&2
    exit 1
  fi
  INFLECT_FLAGS+=" -D$name=$value"
  printf -v "$3" '%s' "$value"
}
append_inflect_int_flag() {
  local name="$1"
  local default_value="$2"
  local value="${!name:-$default_value}"
  if [[ ! "$value" =~ ^[0-9]+$ || "$value" -lt 1 ]]; then
    printf '%s must be a positive integer, got %q\n' "$name" "$value" >&2
    exit 1
  fi
  INFLECT_FLAGS+=" -D$name=$value"
  printf -v "$3" '%s' "$value"
}
append_inflect_nonnegative_int_flag() {
  local name="$1"
  local default_value="$2"
  local value="${!name:-$default_value}"
  if [[ ! "$value" =~ ^[0-9]+$ ]]; then
    printf '%s must be a non-negative integer, got %q\n' "$name" "$value" >&2
    exit 1
  fi
  INFLECT_FLAGS+=" -D$name=$value"
  printf -v "$3" '%s' "$value"
}
append_inflect_symbol_flag() {
  local name="$1"
  local default_value="$2"
  local allowed="$3"
  local value="${!name:-$default_value}"
  if [[ ! "$value" =~ ^[A-Za-z_][A-Za-z0-9_]*$ ]]; then
    printf '%s must be one of [%s], got %q\n' "$name" "$allowed" "$value" >&2
    exit 1
  fi
  case " $allowed " in
    *" $value "*) ;;
    *)
      printf '%s must be one of [%s], got %q\n' "$name" "$allowed" "$value" >&2
      exit 1
      ;;
  esac
  INFLECT_FLAGS+=" -D$name=$value"
  printf -v "$4" '%s' "$value"
}
if [[ "$TTS_APPS" != "0" ]]; then
  INFLECT_FLAGS="-DINFLECT_LOW_MEMORY -include $SKETCH_DIR/tts/ggml_config_arduino.h -I$SKETCH_DIR -I$SKETCH_DIR/tts/InflectNanoTTS/src -I$SKETCH_DIR/tts/InflectNanoTTS/ggml/include -I$SKETCH_DIR/tts/InflectNanoTTS/ggml/src -I$SKETCH_DIR/tts/InflectNanoTTS/ggml/src/ggml-cpu"
  append_inflect_flag INFLECT_USE_ESP_DSP_CONTIG 0 INFLECT_DSP_CONTIG
  append_inflect_flag INFLECT_USE_ESP_DSP_STRIDED 1 INFLECT_DSP_STRIDED
  append_inflect_flag INFLECT_USE_RESBLOCK_IM2COL 1 INFLECT_RESBLOCK_IM2COL
  append_inflect_symbol_flag INFLECT_VOCODER_BACKEND neural "neural griffin_lim" INFLECT_BACKEND
  append_inflect_nonnegative_int_flag INFLECT_GRIFFIN_LIM_ITERS 8 INFLECT_GL_ITERS
  append_inflect_flag INFLECT_PROFILE_ACOUSTIC_OPS 0 INFLECT_ACOUSTIC_PROFILE
  append_inflect_flag INFLECT_PROFILE_VOCODER_OPS 0 INFLECT_VOCODER_PROFILE
  append_inflect_flag INFLECT_ACOUSTIC_SKIP_POSTNET 0 INFLECT_SKIP_POSTNET
  log "TTS enabled: low_memory=1 esp_dsp_contig=$INFLECT_DSP_CONTIG esp_dsp_strided=$INFLECT_DSP_STRIDED resblock_im2col=$INFLECT_RESBLOCK_IM2COL backend=$INFLECT_BACKEND griffin_lim_iters=$INFLECT_GL_ITERS profile_acoustic=$INFLECT_ACOUSTIC_PROFILE profile_vocoder=$INFLECT_VOCODER_PROFILE acoustic_skip_postnet=$INFLECT_SKIP_POSTNET"
else
  log "TTS disabled; set ENABLE_TTS=1 or WITH_TTS=1 to include it"
fi
if [[ "$NETWORK_APPS" != "0" ]]; then
  COMPILE_ARGS+=(--build-property "build.extra_flags=-DENABLE_NETWORK_APPS=1 -DENABLE_TTS=$TTS_APPS $INFLECT_FLAGS")
else
  COMPILE_ARGS+=(--build-property "build.extra_flags=-DENABLE_NETWORK_APPS=0 -DENABLE_TTS=$TTS_APPS $INFLECT_FLAGS")
fi
if [[ "${VERBOSE:-0}" == "1" ]]; then
  COMPILE_ARGS+=(--verbose)
fi
COMPILE_ARGS+=("$@" "$SKETCH_DIR")

log "Compiling firmware"
printf 'Command:'
printf ' %q' "${COMPILE_ARGS[@]}"
printf '\n'
"${COMPILE_ARGS[@]}"

if [[ ! -f "$APP_BIN" ]]; then
  printf 'Expected app binary was not created: %s\n' "$APP_BIN" >&2
  exit 1
fi
if [[ ! -f "$MERGED_BIN" ]]; then
  printf 'Expected merged flash image was not created: %s\n' "$MERGED_BIN" >&2
  exit 1
fi
if [[ ! -f "$PARTITIONS_BIN" ]]; then
  printf 'Expected partition table binary was not created: %s\n' "$PARTITIONS_BIN" >&2
  exit 1
fi

log "Compiled app binary"
ls -lh "$APP_BIN"
ls -lh "$MERGED_BIN"
APP_BIN_SIZE=$(wc -c < "$APP_BIN")
if (( APP_BIN_SIZE > APP_PARTITION_SIZE_BYTES )); then
  printf 'App binary is too large: %d bytes > %d byte factory partition\n' \
    "$APP_BIN_SIZE" "$APP_PARTITION_SIZE_BYTES" >&2
  exit 1
fi
if ! grep -Eq '^[[:space:]]*factory,[[:space:]]*app,[[:space:]]*factory,[[:space:]]*0x10000,[[:space:]]*0x7E0000,' "$PARTITIONS_CSV"; then
  printf 'Unexpected partition layout in %s; app-only flashing assumes factory app at 0x10000 size 0x7E0000.\n' "$PARTITIONS_CSV" >&2
  printf 'Flash a merged image manually after partition changes before using bin/app.sh again.\n' >&2
  exit 1
fi

FLASH_PARTITIONS_READ="$(mktemp "${TMPDIR:-/tmp}/pocketink-partitions.XXXXXX.bin")"
PARTITIONS_BIN_SIZE=$(wc -c < "$PARTITIONS_BIN")
READ_PARTITIONS_ARGS=("$ESPTOOL" --chip esp32s3 -p "$PORT" -b "$UPLOAD_SPEED" --before usb-reset read-flash 0x8000 "$PARTITIONS_BIN_SIZE" "$FLASH_PARTITIONS_READ")

log "Checking flashed partition table"
printf 'Command:'
printf ' %q' "${READ_PARTITIONS_ARGS[@]}"
printf '\n'
"${READ_PARTITIONS_ARGS[@]}"

if cmp -s "$PARTITIONS_BIN" "$FLASH_PARTITIONS_READ"; then
  FLASH_ARGS=("$ESPTOOL" --chip esp32s3 -p "$PORT" -b "$UPLOAD_SPEED" --before usb-reset write-flash 0x10000 "$APP_BIN")
  log "Flashing app binary"
else
  FLASH_ARGS=("$ESPTOOL" --chip esp32s3 -p "$PORT" -b "$UPLOAD_SPEED" --before usb-reset write-flash 0x0 "$MERGED_BIN")
  log "Partition table changed; flashing merged firmware image"
fi
printf 'Command:'
printf ' %q' "${FLASH_ARGS[@]}"
printf '\n'
"${FLASH_ARGS[@]}"

log "Done"
